    static async getAll() {
        try {
            const { data, error } = await supabaseClient
                .from('company_policy')
                .select('*')
                .order('id', { ascending: true });

            if (error) throw error;
            return (data || []).map(d => new CompanyPolicy(d));
        } catch (error) {
            console.error('Erro ao buscar políticas da empresa:', error);
            return [];
        }
    }

    static async getById(id) {
        try {
            const { data, error } = await supabaseClient
                .from('company_policy')
                .select('*')
                .eq('id', id)
                .single();

            if (error) throw error;
            return data ? new CompanyPolicy(data) : null;
        } catch (error) {
            console.error('Erro ao buscar política:', error);
            return null;
        }
    }

    static async create(title, description, icon = 'document') {
        try {
            const { data, error } = await supabaseClient
                .from('company_policy')
                .insert([{ title, description, icon }])
                .select()
                .single();

            if (error) throw error;
            return new CompanyPolicy(data);
        } catch (error) {
            console.error('Erro ao criar política:', error);
            throw error;
        }
    }

    static async update(id, title, description, icon = 'document') {
        try {
            const { data, error } = await supabaseClient
                .from('company_policy')
                .update({ title, description, icon, updated_at: new Date().toISOString() })
                .eq('id', id)
                .select()
                .single();

            if (error) throw error;
            return new CompanyPolicy(data);
        } catch (error) {
            console.error('Erro ao atualizar política:', error);
            throw error;
        }
    }

    static async delete(id) {
        try {
            const { error } = await supabaseClient
                .from('company_policy')
                .delete()
                .eq('id', id);

            if (error) throw error;
        } catch (error) {
            console.error('Erro ao excluir política:', error);
            throw error;
        }
    }
